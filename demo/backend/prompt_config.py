def get_prompt_template(nodule_type: str, prompt_type: str) -> str:
    """
    获取指定结节类型和提示词类型的模板

    Args:
        nodule_type: 结节类型 (breast/lung/thyroid/breast_lung/breast_thyroid/lung_thyroid/triple)
        prompt_type: 提示词类型 (imaging/western_medical)

    Returns:
        str: 提示词模板字符串

    Raises:
        ValueError: 如果结节类型或提示词类型不存在
    """
    if nodule_type not in PROMPTS:
        raise ValueError(f"不支持的结节类型: {nodule_type}。支持的类型: {list(PROMPTS.keys())}")

    if prompt_type not in PROMPTS[nodule_type]:
        raise ValueError(f"不支持的提示词类型: {prompt_type}。支持的类型: {list(PROMPTS[nodule_type].keys())}")

    return PROMPTS[nodule_type][prompt_type]


